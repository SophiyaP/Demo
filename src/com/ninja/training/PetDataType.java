package com.ninja.training;

public class PetDataType {

	public static void main(String[] args) {
		String myPet = "DOG";
		String petName = "Spott";

		// char: Stores Single Character
		char petInitial = 'S';

		//int: Stores Integers (Whole Numbers)
		int petAge = 5; // My Dog is 5 years old.

		//float: Stores Decimals
		double  petWeight = 10.25d; // 10.25 kilograms 

		//boolean: true or false
		boolean isPetVaccinated = true;
		boolean willPetBite = false;
		System.out.println("I have a pet " +myPet);
		System.out.println("My pet name is " +petName);
		System.out.println("My pet initial is " +petInitial);
		System.out.println("My pet's age is " +petAge);
		System.out.println("My pet's weight is " +petWeight);
		System.out.println("Is my Pet vaccinated " +isPetVaccinated);
		System.out.println("Will my pet bite " +willPetBite);
		  }
		}


